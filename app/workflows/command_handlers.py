# ============================================================================
# Command: /explain - Explain PR Changes
# ============================================================================

async def handle_explain_command(event: IssueCommentEvent) -> None:
    """
    Handle /explain command with Explainer Agent personality.

    Supports:
    - /explain -> Explain PR diff
    - /explain app/main.py -> Explain specific file
    - /explain app/main.py:MyClass -> Explain specific class/function

    Args:
        event: Issue comment event containing the command
    """
    repo_name = event.repository.full_name
    pr_number = event.issue.number
    installation_id = event.installation.id
    comment_body = event.comment.body.strip()

    logger.info(
        f"Handling /explain command for {repo_name}#{pr_number}",
        extra={
            "extra_fields": {
                "repo_name": repo_name,
                "pr_number": pr_number,
                "commenter": event.comment.user.login,
                "comment": comment_body,
            }
        }
    )

    start_time = datetime.utcnow()

    try:
        # Initialize clients
        github_client = GitHubClient()
        gemini_client = GeminiClient(use_flash=True)

        # Initialize Explainer Agent
        explainer = ExplainerAgent(gemini_client, github_client)

        # Parse command to extract file reference (if any)
        # Command format: /explain [optional: file_path or file_path:target]
        parts = comment_body.split(maxsplit=1)
        file_reference = parts[1].strip() if len(parts) > 1 else None

        # Fetch PR details for context
        pr_details = github_client.get_pr_details(
            repo_name=repo_name,
            pr_number=pr_number,
            installation_id=installation_id
        )

        pr_title = pr_details.get('title', '')
        pr_description = pr_details.get('body', '')
        head_ref = pr_details.get('head', {}).get('sha', 'HEAD')

        # Generate explanation based on what was requested
        if file_reference:
            # Explain specific file or function/class
            logger.info(f"Explaining reference: {file_reference}")
            response = await explainer.explain_from_reference(
                repo_name=repo_name,
                reference=file_reference,
                installation_id=installation_id,
                pr_number=pr_number,
                context=f"From PR #{pr_number}: {pr_title}",
                pr_title=pr_title,
                ref=head_ref,
            )
        else:
            # Explain entire PR diff
            logger.info(f"Explaining PR diff for #{pr_number}")
            response = await explainer.explain_pr_diff(
                repo_name=repo_name,
                pr_number=pr_number,
                installation_id=installation_id,
                pr_title=pr_title,
                pr_description=pr_description,
            )

        # Calculate metrics
        end_time = datetime.utcnow()
        duration = (end_time - start_time).total_seconds()

        # Format response comment (explanation already includes formatting and emojis)
        comment = f"""{response.explanation}

---
<details>
<summary>üìä Generation Metadata</summary>

- **Model:** {gemini_client.model_config.model_name}
- **Tokens Used:** {format_tokens(response.tokens_used)}
- **Cost:** {format_cost(response.cost_usd)}
- **Generation Time:** {format_duration(duration)}

</details>

*Generated by RepoAuditor AI's Explainer Agent ü§ñ*
"""

        # Post explanation comment
        github_client.post_pr_comment(
            repo_name=repo_name,
            pr_number=pr_number,
            body=comment,
            installation_id=installation_id
        )

        logger.info(
            f"Posted explanation for PR #{pr_number}",
            extra={
                "extra_fields": {
                    "repo_name": repo_name,
                    "pr_number": pr_number,
                    "file_reference": file_reference,
                    "duration_seconds": duration,
                    "cost_usd": response.cost_usd,
                    "tokens_used": response.tokens_used,
                }
            }
        )

    except Exception as e:
        error_context = extract_error_context(e, repo_name, pr_number)

        logger.error(
            f"Failed to handle /explain command: {e}",
            exc_info=True,
            extra={"extra_fields": error_context}
        )

        # Post error comment with personality
        try:
            github_client = GitHubClient()

            # Get list of changed files to help user
            try:
                pr_diff = github_client.get_pr_diff(
                    repo_name=repo_name,
                    pr_number=pr_number,
                    installation_id=installation_id,
                )
                from app.utils.code_fetcher import CodeFetcher
                fetcher = CodeFetcher(github_client)
                changed_files = fetcher.extract_changed_files_from_diff(pr_diff)
                files_list = "\n".join(f"- `{f}`" for f in changed_files[:10])
            except:
                changed_files = []
                files_list = ""

            error_comment = f"""## ‚ùå Oops! Couldn't Find That File

Hey! I tried to explain **`{file_reference or 'the PR'}`**, but couldn't locate it.

**The issue:**
```
{str(e)}
```

**Quick fixes to try:**
"""

            if changed_files:
                error_comment += f"""
**Files in this PR:**
{files_list}

Try one of these instead! For example: `/explain {changed_files[0]}`
"""
            else:
                error_comment += """
- Double-check the file path (case-sensitive!)
- Make sure the file exists in this branch
- Try `/explain` without a file name to explain the whole PR
"""

            error_comment += """
**Pro Tips:**
- Use tab completion in your file explorer to get the exact path
- File paths are relative to the repo root (no leading `/`)
- Try `/explain` (no arguments) to see all changes in this PR

*Need help? Just ask!* üí¨
"""
            github_client.post_pr_comment(
                repo_name=repo_name,
                pr_number=pr_number,
                body=error_comment,
                installation_id=installation_id
            )
        except Exception as post_error:
            logger.error(f"Failed to post error comment: {post_error}")


# ============================================================================
# Command: /review - Trigger On-Demand Review
# ============================================================================

async def handle_review_command(event: IssueCommentEvent) -> None:
    """
    Handle /review command to trigger a code review on demand.

    Args:
        event: Issue comment event containing the command
    """
    repo_name = event.repository.full_name
    pr_number = event.issue.number
    installation_id = event.installation.id

    logger.info(
        f"Handling /review command for {repo_name}#{pr_number}",
        extra={
            "extra_fields": {
                "repo_name": repo_name,
                "pr_number": pr_number,
                "commenter": event.comment.user.login,
            }
        }
    )

    try:
        # Initialize GitHub client
        github_client = GitHubClient()

        # Post acknowledgment comment
        ack_comment = f"""## ü§ñ Code Review Initiated

@{event.comment.user.login} requested a code review. Starting analysis now...

*This may take a minute or two depending on the size of the PR.*
"""

        github_client.post_pr_comment(
            repo_name=repo_name,
            pr_number=pr_number,
            body=ack_comment,
            installation_id=installation_id
        )

        # Fetch PR details
        pr_details = github_client.get_pr_details(
            repo_name=repo_name,
            pr_number=pr_number,
            installation_id=installation_id
        )

        # Execute full code review workflow
        logger.info(f"Starting code review workflow for PR #{pr_number}")

        result = await execute_code_review_workflow(
            repo_name=repo_name,
            pr_number=pr_number,
            installation_id=installation_id,
            pr_title=pr_details.get("title", ""),
            pr_author=pr_details.get("user", {}).get("login", "unknown"),
            pr_description=pr_details.get("body", ""),
            commit_sha=pr_details.get("head", {}).get("sha", ""),
        )

        if result.get("error"):
            logger.error(
                f"Review workflow failed for PR #{pr_number}: {result['error']}",
                extra={
                    "extra_fields": {
                        "repo_name": repo_name,
                        "pr_number": pr_number,
                        "error": result["error"],
                    }
                }
            )
        else:
            logger.info(
                f"Review workflow completed for PR #{pr_number}",
                extra={
                    "extra_fields": {
                        "repo_name": repo_name,
                        "pr_number": pr_number,
                        "findings_count": len(result.get("review_results", [])),
                    }
                }
            )

    except Exception as e:
        error_context = extract_error_context(e, repo_name, pr_number)

        logger.error(
            f"Failed to handle /review command: {e}",
            exc_info=True,
            extra={"extra_fields": error_context}
        )

        # Post error comment
        try:
            github_client = GitHubClient()
            error_comment = f"""## ‚ùå Review Failed

Sorry, I encountered an error while running the code review:

```
{str(e)}
```

Please try again or contact support if the issue persists.
"""
            github_client.post_pr_comment(
                repo_name=repo_name,
                pr_number=pr_number,
                body=error_comment,
                installation_id=installation_id
            )
        except Exception as post_error:
            logger.error(f"Failed to post error comment: {post_error}")


